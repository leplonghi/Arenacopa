
-- Create matches table
CREATE TABLE IF NOT EXISTS public.matches (
    id TEXT PRIMARY KEY,
    stage TEXT NOT NULL CHECK (stage IN ('group', 'round_of_32', 'round_of_16', 'qf', 'sf', 'third_place', 'final')),
    group_id TEXT,
    match_date TIMESTAMPTZ NOT NULL,
    home_team_code TEXT NOT NULL,
    away_team_code TEXT NOT NULL,
    home_score INTEGER,
    away_score INTEGER,
    venue_id TEXT NOT NULL,
    status TEXT NOT NULL CHECK (status IN ('scheduled', 'live', 'finished')) DEFAULT 'scheduled',
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- Enable RLS
ALTER TABLE public.matches ENABLE ROW LEVEL SECURITY;

-- Create public read policy
CREATE POLICY "Public read access" ON public.matches FOR SELECT USING (true);

-- Create updated_at trigger
CREATE TRIGGER update_matches_updated_at BEFORE UPDATE ON public.matches FOR EACH ROW EXECUTE FUNCTION public.update_updated_at();

